var	SIGN_BIT	= (0x80);		/* Sign bit for a A-law byte. */
var	QUANT_MASK	= (0xf);		/* Quantization field mask. */
var	NSEGS		= (8);		/* Number of A-law segments. */
var	SEG_SHIFT	= (4);		/* Left shift for segment number. */
var	SEG_MASK	= (0x70);		/* Segment field mask. */

var seg_end = [0xFF, 0x1FF, 0x3FF, 0x7FF,
			    0xFFF, 0x1FFF, 0x3FFF, 0x7FFF];

function search(val, table, size)
{
	var i;
	for (i = 0; i < size; i++) {
		if (val <= table[i])
			return (i);
	}
	return (size);//
}

function linear2alaw(pcm_val)	/* 2's complement (16-bit range) */
{
	var mask;
	var seg;
	var aval;

	if (pcm_val >= 0) {
		mask = 0xD5;		/* sign (7th) bit = 1 */
	} else {
		mask = 0x55;		/* sign bit = 0 */
		pcm_val = -pcm_val - 8;

		/* https://trac.pjsip.org/repos/ticket/1301 
		 * Thank you K Johnson - Zetron - 27 May 2011
		 */
		if (pcm_val < 0)
		    pcm_val = 0;
	}

	/* Convert the scaled magnitude to segment number. */
	seg = search(pcm_val, seg_end, 8);

	/* Combine the sign, segment, and quantization bits. */

	if (seg >= 8)		/* out of range, return maximum value. */
		return (0x7F ^ mask);
	else {
		aval = seg << SEG_SHIFT;
		if (seg < 2)
			aval |= (pcm_val >> 4) & QUANT_MASK;
		else
			aval |= (pcm_val >> (seg + 3)) & QUANT_MASK;
		return (aval ^ mask);
	}
}

/*
 * alaw2linear() - Convert an A-law value to 16-bit linear PCM
 *
 */
function alaw2linear(a_val)
{
	var		t;
	var		seg;

	a_val ^= 0x55;

	t = (a_val & QUANT_MASK) << 4;
	seg = (a_val & SEG_MASK) >> SEG_SHIFT;
	switch (seg) {
	case 0:
		t += 8;
		break;
	case 1:
		t += 0x108;
		break;
	default:
		t += 0x108;
		t <<= seg - 1;
	}
	return ((a_val & SIGN_BIT) ? t : -t);
}

